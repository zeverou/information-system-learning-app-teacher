-- ============================================================
-- SCHEMA
-- ============================================================

CREATE TABLE alergeny (
    id_alergenu INTEGER PRIMARY KEY AUTOINCREMENT,
    jmeno TEXT
);

CREATE TABLE jidla (
    id_jidla INTEGER PRIMARY KEY AUTOINCREMENT,
    jmeno TEXT,
    doba_podavani TEXT
);

CREATE TABLE jidla_alergeny (
    id_alergenu INTEGER,
    id_jidla INTEGER,
    PRIMARY KEY (id_alergenu, id_jidla)
);

CREATE TABLE kniha_jidel (
    id_jidla INTEGER,
    datum DATE,
    PRIMARY KEY (id_jidla, datum)
);

CREATE TABLE turnusy (
    id_turnusu INTEGER PRIMARY KEY AUTOINCREMENT,
    datum_od DATE,
    datum_do DATE,
    kapacita INTEGER
);

CREATE TABLE ucastnici (
    id_ucastnika INTEGER PRIMARY KEY AUTOINCREMENT,
    jmeno TEXT,
    email TEXT,
    rodne_cislo TEXT,
    telefon TEXT,
    adresa TEXT,
    vek INTEGER
);

CREATE TABLE turnusy_ucastnici (
    id_ucastnika INTEGER,
    id_turnusu INTEGER,
    PRIMARY KEY (id_ucastnika, id_turnusu)
);

CREATE TABLE ucastnici_alergeny (
    id_ucastnika INTEGER,
    id_alergenu INTEGER,
    PRIMARY KEY (id_ucastnika, id_alergenu)
);

CREATE TABLE ucastnici_jidla (
    id_jidla INTEGER,
    id_ucastnika INTEGER,
    datum_podavani DATE,
    PRIMARY KEY (id_jidla, id_ucastnika, datum_podavani)
);

CREATE TABLE vedouci (
    id_vedouciho INTEGER PRIMARY KEY AUTOINCREMENT,
    jmeno TEXT,
    email TEXT,
    rodne_cislo TEXT,
    telefon TEXT,
    adresa TEXT,
    vek INTEGER
);

CREATE TABLE vedouci_alergeny (
    id_vedouciho INTEGER,
    id_alergenu INTEGER,
    PRIMARY KEY (id_vedouciho, id_alergenu)
);

CREATE TABLE vedouci_turnusy (
    id_turnusu INTEGER,
    id_vedouciho INTEGER,
    PRIMARY KEY (id_turnusu, id_vedouciho)
);

CREATE TABLE jidla_vedouci (
    id_jidla INTEGER,
    id_vedouciho INTEGER,
    datum_podavani DATE,
    PRIMARY KEY (id_jidla, id_vedouciho, datum_podavani)
);

